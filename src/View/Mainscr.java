    private void tableCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCartMouseClicked
        // TODO add your handling code here:
        int index = tableCart.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this product?", "", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            double itemTotal = (Double) ((DefaultTableModel) tableCart.getModel()).getValueAt(index, 2);
            grandTotal -= itemTotal;
            txtGrandTotal.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) tableCart.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_tableCartMouseClicked

    private void btnClrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClrActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Do you want to clear cart?", "", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            ((DefaultTableModel) tableCart.getModel()).setRowCount(0);
            clearFields();
            grandTotal = 0.0;
            txtGrandTotal.setText(String.valueOf(grandTotal));
            btnClr.setEnabled(false);
            btnCheckOut.setEnabled(false);
        }
    }//GEN-LAST:event_btnClrActionPerformed

    private void cbCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCategoryActionPerformed
        // TODO add your handling code here:
        String categoryName = (String) cbCategory.getSelectedItem();
        Category category = new Category();
        category.setName(categoryName);
        category.setId(categories.getCategoryByName(categoryName));
        itemNamebyCategory(category.getId());
    }//GEN-LAST:event_cbCategoryActionPerformed

    private void clearFields() {
        totalCost = 0.0;
        price = 0.0;
        itemName = "";
        txtPrice.setText("");
        spinnerQty.setValue(0);
        txtTotal.setText("");
        btnAddtoCart.setEnabled(false);
    }

