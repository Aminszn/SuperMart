    private void tableProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductsMouseClicked
        // TODO add your handling code here:
        int index = tableProducts.getSelectedRow();
        TableModel model = tableProducts.getModel();
        String itemname = model.getValueAt(index, 0).toString();
        Item cartItem = items.getItemByname(itemname);
        itemName = cartItem.getName();
        totalCost = cartItem.getPrice();
        price = cartItem.getPrice();
        txtPrice.setText(String.valueOf(price));
        spinnerQty.setValue(cartItem.getQuantity());
        txtTotal.setText(String.valueOf(totalCost));
        btnAddtoCart.setEnabled(true);
    }//GEN-LAST:event_tableProductsMouseClicked

    private void spinnerQtyStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spinnerQtyStateChanged
        // TODO add your handling code here:
        itemQuantity = (Integer) spinnerQty.getValue();
        if (itemQuantity <= 1) {
            spinnerQty.setValue(1);
            itemQuantity = 1;
        }
        totalCost = price * itemQuantity;
        txtTotal.setText(String.valueOf(totalCost));
    }//GEN-LAST:event_spinnerQtyStateChanged

    private void btnAddtoCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddtoCartActionPerformed
        // TODO add your handling code here:
        Item item = new Item();
        item.setName(itemName);
        item.setTotal(Double.parseDouble(txtTotal.getText()));
        item.setQuantity((Integer) spinnerQty.getValue());
        clearFields();
        DefaultTableModel dtm = (DefaultTableModel) tableCart.getModel();
        dtm.addRow(new Object[]{item.getName(), item.getQuantity(), item.getTotal()});
        grandTotal += item.getTotal();
        txtGrandTotal.setText(String.valueOf(grandTotal));
        btnClr.setEnabled(true);
        btnCheckOut.setEnabled(true);
    }//GEN-LAST:event_btnAddtoCartActionPerformed

