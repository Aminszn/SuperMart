    private void itemNamebyCategory(int categoryId) {
        DefaultTableModel dtm = (DefaultTableModel) tableProducts.getModel();
        dtm.setRowCount(0);
        ArrayList<Item> items1 = items.getItemsByCategoryid(categoryId);
        Iterator<Item> itr = items1.iterator();
        while (itr.hasNext()) {
            Item item = itr.next();
            dtm.addRow(new Object[]{item.getName()});
        }
    }

    private void populateCategory() {

        ArrayList<Category> cat = categories.getCategories();
        DefaultComboBoxModel dml = (DefaultComboBoxModel) cbCategory.getModel();
        dml.removeAllElements();
        for (int i = 0; i < cat.size(); i++) {
            dml.addElement(cat.get(i).getName());
        }

    }

